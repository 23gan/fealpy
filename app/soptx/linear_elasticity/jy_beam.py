def rotate_point(point, rotation_axis, angle_deg, translation):
    """
    Rotate a point around a given axis by a specified angle and apply translation.

    :param point: ndarray, shape (3,), the original point coordinates [x, y, z]
    :param rotation_axis: ndarray, shape (3,), the rotation axis [ux, uy, uz], should be a unit vector
    :param angle_deg: float, the rotation angle in degrees
    :param translation: ndarray, shape (3,), the translation vector [tx, ty, tz]
    :return: ndarray, shape (3,), the transformed coordinates
    """
    angle_rad = angle_deg / 180 * bm.pi
    cos_theta = bm.cos(angle_rad)
    sin_theta = bm.sin(angle_rad)
    ux, uy, uz = rotation_axis

    # Rotation matrix
    R = bm.array([
        [cos_theta + ux ** 2 * (1 - cos_theta), ux * uy * (1 - cos_theta) - uz * sin_theta,
         ux * uz * (1 - cos_theta) + uy * sin_theta],
        [uy * ux * (1 - cos_theta) + uz * sin_theta, cos_theta + uy ** 2 * (1 - cos_theta),
         uy * uz * (1 - cos_theta) - ux * sin_theta],
        [uz * ux * (1 - cos_theta) - uy * sin_theta, uz * uy * (1 - cos_theta) + ux * sin_theta,
         cos_theta + uz ** 2 * (1 - cos_theta)]
    ])

    # Apply translation and rotation
    translated_point = point - translation  # Subtract translation
    rotated_point = bm.dot(R, translated_point.T).T  # Apply rotation
    return rotated_point + translation  # Add translation back


